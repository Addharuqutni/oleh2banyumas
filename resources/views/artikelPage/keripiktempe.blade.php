                <div class="article-text">
                    <h3>Cara Membuat</h3>
                    <ol class="preparation-steps">
                        <li>Pilih tempe dengan kualitas baik dan tanpa jamur berlebih. Iris tipis tempe dengan ketebalan
                            sekitar 0,5 - 1 mm. Direkomendasikan untuk mengiris tempe saat akan digoreng saja karena jamur
                            tempe dapat berkembang dengan cepat.</li>
                        <li>Siapkan adonan tepung dengan mencampurkan tepung beras, tepung tapioka, baking powder, dan semua
                            bumbu dalam wadah. Aduk rata.</li>
                        <li>Tambahkan air secukupnya ke dalam campuran tepung hingga membentuk adonan yang tidak terlalu
                            kental dan tidak terlalu encer. Konsistensinya harus bisa melapisi tempe dengan baik.</li>
                        <li>Celupkan irisan tempe ke dalam adonan tepung berbumbu hingga seluruh permukaan terlapisi rata.
                        </li>
                        <li>Panaskan minyak dalam wajan dengan api sedang. Pastikan menggunakan minyak yang baru untuk hasil
                            yang maksimal.</li>
                        <li>Goreng tempe yang sudah dilapisi adonan hingga setengah matang, kemudian angkat dan tiriskan.
                        </li>
                        <li>Setelah semua tempe digoreng tahap pertama, goreng kembali tempe untuk kedua kalinya hingga
                            berwarna kecoklatan dan benar-benar renyah.</li>
                        <li>Angkat keripik tempe, tiriskan minyaknya, dan dinginkan sebelum dikemas agar tetap renyah.</li>
                    </ol>

                    <div class="article-tip">
                        <p><strong>Tip:</strong> Kunci utama dalam membuat Keripik Tempe Banyumas yang renyah adalah
                            ketebalan irisan tempe yang konsisten, penggunaan minyak baru, dan penggorengan dua kali.
                            Pastikan tempe benar-benar kering setelah digoreng kedua kali untuk mendapatkan tekstur renyah
                            yang tahan lama. Jika ingin membuat varian rasa, tambahkan bumbu seperti bubuk cabai untuk rasa
                            pedas atau gula untuk rasa manis setelah keripik diangkat dari penggorengan kedua.</p>
