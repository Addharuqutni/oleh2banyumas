                    <div class="ingredient-card">
                        <h3>Bumbu</h3>
                        <ul>
                            <li>5 siung bawang putih, haluskan</li>
                            <li>2 sendok teh ketumbar bubuk</li>
                            <li>5 lembar daun jeruk, iris halus</li>
                            <li>1 sendok teh garam</li>
                            <li>1/2 sendok teh merica bubuk</li>
                            <li>1 sendok teh kaldu bubuk</li>
                        </ul>
