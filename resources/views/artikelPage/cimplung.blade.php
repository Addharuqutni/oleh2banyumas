            <section class="article-section">
                <h2>Bahan dan Cara Pembuatan</h2>

                <div class="ingredient-container">
                    <div class="ingredient-card">
                        <h3>Bahan Utama</h3>
                        <ul>
                            <li>500 gram singkong berkualitas baik</li>
                            <li>250 gram gula merah, sisir halus</li>
                            <li>3 sendok makan gula pasir</li>
                            <li>1/4 sendok teh garam</li>
                            <li>1 liter air</li>
                        </ul>
                    </div>

                    <div class="ingredient-card">
                        <h3>Bahan Tambahan</h3>
                        <ul>
                            <li>2-3 lembar daun pandan</li>
                            <li>1 batang kayu manis</li>
                            <li>1 batang serai, geprek</li>
                            <li>Santan kelapa (opsional)</li>
                            <li>Wijen atau kacang tanah untuk taburan (opsional)</li>
                        </ul>
                    </div>
                </div>

                <div class="article-text">
                    <h3>Cara Membuat</h3>
                    <ol class="preparation-steps">
                        <li>Kupas singkong dan cuci bersih, lalu potong-potong sesuai selera. Pilih singkong yang bagus dan
                            tidak
                            terlalu tua agar hasilnya lembut.</li>
                        <li>Siapkan panci dan tuang air, masukkan daun pandan, kayu manis, dan serai. Didihkan.</li>
                        <li>Setelah air mendidih, masukkan potongan singkong, rebus hingga setengah matang.</li>
                        <li>Tambahkan gula merah, gula pasir, dan garam. Aduk rata dan masak dengan api kecil.</li>
                        <li>Jika ingin lebih kaya rasa, tambahkan santan kelapa secukupnya.</li>
                        <li>Masak hingga air menyusut dan gula meresap ke dalam singkong, sekitar 30-45 menit. Singkong yang
                            sudah
                            empuk menandakan cimplung sudah matang.</li>
                    </ol>

                    <div class="article-tip">
                        <p><strong>Tip:</strong> Kunci utama dalam membuat cimplung yang lezat adalah kualitas singkong dan
                            gula
                            merah. Singkong yang baik akan menghasilkan tekstur yang lembut, sementara gula merah
                            berkualitas akan
                            memberikan rasa manis alami yang khas. Jika tidak ada air nira (badheg), kombinasi gula merah
                            dan air
                            biasa sudah cukup untuk memberikan cita rasa yang mirip.</p>
                    </div>
                </div>
